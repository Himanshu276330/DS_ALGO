package HARRY_2021;

public class CWH_21_ch_05_loop {
    public static void main(String[] args) {


        //While loop
        int i = 1;
        while (i <= 9) {
            System.out.print(i++);
        }

        int i2 = 1;
        while (i2 <= 9) {
            System.out.println(i2);
            i2++;
        }



    }
}
